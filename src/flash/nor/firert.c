unsigned int app_len = 15948;

/* ---- 根据你的链接脚本一致化这些常量 ---- */
#define OCRAM_START     0x20200000U
#define OCRAM_END       0x20240000U   /* 256KB OCRAM 顶(默认 FlexRAM 配置) */
#define VTOR_ADDR       0xE000ED08U

/* 最小对齐与安全边界 */
#define SP_ALIGN        8U
#define MIN_STACK_GUARD 0x100U        /* 留 256B 防止把 MSP 落太靠近数据段 */

static inline uint32_t align_down(uint32_t v, uint32_t a) { return v & ~(a - 1U); }
static inline bool in_range(uint32_t x, uint32_t lo, uint32_t hi_excl)
{
    return (x >= lo) && (x < hi_excl);
}

static int firert_stub_run(struct target *target)
{
    int retval = ERROR_OK;

    retval = target_write_buffer(target, CODE_BASE_ADDR, app_len, app);
    if (retval != ERROR_OK) {
        LOG_ERROR(LOG_PREFIX ": write app to OCRAM failed (%d)", retval);
        return retval;
    }

    uint32_t msp = 0, pc = 0;
    retval = target_read_u32(target, CODE_BASE_ADDR + 0, &msp);
    if (retval != ERROR_OK) { LOG_ERROR(LOG_PREFIX ": read MSP failed"); return retval; }
    retval = target_read_u32(target, CODE_BASE_ADDR + 4, &pc);
    if (retval != ERROR_OK) { LOG_ERROR(LOG_PREFIX ": read PC failed");  return retval; }

    if ((pc & 1U) == 0) {
        LOG_WARNING(LOG_PREFIX ": reset PC LSB not set (0x%08x) -> force Thumb", pc);
        pc |= 1U;
    }
    msp = align_down(msp, SP_ALIGN);

    const uint32_t msp_min = OCRAM_START + MIN_STACK_GUARD;
    const uint32_t msp_max = OCRAM_END;                    /* 栈顶可等于区间上界 */
    if (!in_range(msp, msp_min, msp_max + 1U)) {
        LOG_WARNING(LOG_PREFIX ": vector MSP 0x%08x out of OCRAM -> use OCRAM_END", msp);
        msp = align_down(OCRAM_END, SP_ALIGN);
    }

    const uint32_t exec_lo = CODE_BASE_ADDR;
    const uint32_t exec_hi = OCRAM_END;                    /* 代码/数据都在 m_stub */
    if (!in_range(pc & ~1U, exec_lo, exec_hi)) {
        LOG_WARNING(LOG_PREFIX ": reset PC 0x%08x not in OCRAM exec range [%08x, %08x), force to entry",
                    pc, exec_lo, exec_hi);
        pc = (exec_lo | 1U);
    }

    retval = target_write_u32(target, VTOR_ADDR, CODE_BASE_ADDR);
    if (retval != ERROR_OK) {
        LOG_ERROR(LOG_PREFIX ": write VTOR failed (%d)", retval);
        return retval;
    }

    struct reg_param regs[3];
    int nregs = 0;

    init_reg_param(&regs[nregs], "msp", 32, PARAM_OUT);
    buf_set_u32(regs[nregs].value, 0, 32, msp);
    nregs++;

    init_reg_param(&regs[nregs], "pc", 32, PARAM_OUT);
    buf_set_u32(regs[nregs].value, 0, 32, pc);
    nregs++;

    init_reg_param(&regs[nregs], "control", 32, PARAM_OUT);
    buf_set_u32(regs[nregs].value, 0, 32, 0U);
    nregs++;

    struct armv7m_algorithm algo;
    memset(&algo, 0, sizeof(algo));
    algo.common_magic = ARMV7M_COMMON_MAGIC;
    algo.core_mode    = ARM_MODE_THUMB;   /* Cortex-M 必须 Thumb */

    target_resume(target, 0 /* current addr unused */, (pc & ~1u) /* entry */, 1, 1);

    int rv = target_wait_state(target, TARGET_HALTED, /*ms*/2000);
    if (rv == ERROR_TIMEOUT_REACHED) {
        alive_sleep(2000);              /* 这是 OpenOCD 自带的 sleep */
        (void)target_halt(target);
        rv = target_wait_state(target, TARGET_HALTED, /*ms*/2000);
        if (rv != ERROR_OK) return rv;
    } else if (rv != ERROR_OK) {
        return rv;
    }

    /* 同步一次内部状态机 */
    rv = target_poll(target);
    if (rv != ERROR_OK) return rv;

    /* 清理 */
    for (int i = 0; i < nregs; i++) destroy_reg_param(&regs[i]);

    return ERROR_OK;
}

static int firert_flash_write(struct flash_bank *bank,
                              const uint8_t *buffer,
                              uint32_t offset,
                              uint32_t count)
{
    if (count == 0) return ERROR_OK;

    struct target *t = bank->target;
    int rv;

    uint32_t done = 0;
    while (done < count) {
        uint32_t chunk = count - done;
        if (chunk > CHUNK_MAX) chunk = CHUNK_MAX;

        uint32_t dst_abs = (uint32_t)bank->base + offset + done;

        /* 把本片数据灌到 OCRAM 的 SRC_BUF */
        if ((rv = target_write_buffer(t, SRC_BUF, chunk, buffer + done)) != ERROR_OK) return rv;

        /* 填 mailbox:SRC / DST / SIZE|GO */
        if ((rv = target_write_u32(t, MB_SRC_ADDR, SRC_BUF)) != ERROR_OK) return rv;
        if ((rv = target_write_u32(t, MB_DST_ADDR, dst_abs)) != ERROR_OK) return rv;
        if ((rv = target_write_u32(t, MB_SIZE_ADDR, (chunk | 0x80000000u))) != ERROR_OK) return rv;

        /* 等效 telnet> resume(不改 PC) */
        if ((rv = target_poll(t)) != ERROR_OK) return rv;              /* 同步内部状态机 */
        if (t->state != TARGET_HALTED) return ERROR_TARGET_NOT_HALTED; /* 进入时必须 HALT */

        if ((rv = target_resume(t, /*current*/1, /*addr*/0, /*handle_bkpt*/1, /*debug*/0)) != ERROR_OK)
            return rv;

        /* 等它在 stub 的 BKPT 停住(完成一片) */
        if ((rv = target_wait_state(t, TARGET_HALTED, 15000)) != ERROR_OK) return rv;

        /* 读取并打印 MB_SIZE(0=成功,非零=错误码) */
        uint32_t stat = 0;
        if ((rv = target_read_u32(t, MB_SIZE_ADDR, &stat)) != ERROR_OK) return rv;
        LOG_INFO(LOG_PREFIX ": chunk %uB -> 0x%08" PRIx32 " MB_SIZE=0x%08" PRIx32, chunk, dst_abs, stat);
        if (stat != 0) return ERROR_FAIL;

        /* 跨过这条 BKPT(否则再 resume 还会马上停在同一处) */
        if ((rv = target_step(t, /*current*/1, /*addr*/0, /*handle_bkpt*/1)) != ERROR_OK) return rv;
        /* 可选再等一下,确保步进完成 */
        if ((rv = target_wait_state(t, TARGET_HALTED, 100)) != ERROR_OK) return rv;

        /* 这次真的写下一片了 */
        done += chunk;
    }

    return ERROR_OK;
}
    


